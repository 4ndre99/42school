int	ft_iterative_factorial(int nb)
{
	int	temp;

	temp = nb;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (temp > 1)
	{
		nb *= (temp - 1);
		temp--;
	}
	return (nb);
}
